package com.sinosoft.common.util;

public class ChartModel {
	private String x;//x轴的值
	private String y;//y轴的值
	private String title;//图形的标题
	private String name;//鼠标放上去显示的名称
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
