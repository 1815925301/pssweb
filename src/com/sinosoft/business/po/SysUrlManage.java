package com.sinosoft.business.po;


public class SysUrlManage {
	
	
    //id       数据库字段: ID 
	private Long id;
	
	
    //父id       数据库字段: F_ID 
	private Long fid;
	
	
    //中文描述       数据库字段: NAME_CN 
	private java.lang.String nameCn;
	
	
    //英文描述       数据库字段: NAME_EN 
	private java.lang.String nameEn;
	
	
    //url       数据库字段: URL 
	private java.lang.String url;


	public Long getId() {
		return id;
	}


	public void setId(Long id) {
		this.id = id;
	}


	public Long getFid() {
		return fid;
	}


	public void setFid(Long fid) {
		this.fid = fid;
	}


	public java.lang.String getNameCn() {
		return nameCn;
	}


	public void setNameCn(java.lang.String nameCn) {
		this.nameCn = nameCn;
	}


	public java.lang.String getNameEn() {
		return nameEn;
	}


	public void setNameEn(java.lang.String nameEn) {
		this.nameEn = nameEn;
	}


	public java.lang.String getUrl() {
		return url;
	}


	public void setUrl(java.lang.String url) {
		this.url = url;
	}
	
	
 

}
